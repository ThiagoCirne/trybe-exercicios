let resultado = 'Aprovada';

switch (resultado) {
    case 'Aprovada':
        break;
        console.log("Parabén, você está no grupo de pessoas aprovadas!");
    case 'Lista':
        break;
        console.log("Você está na nossa lista de espera");
    case 'reprovada':
        break;
        console.log("Infelizmente, você reprovou");  
    default:
        console.log("Informação incorreta"); 
}